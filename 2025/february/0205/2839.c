#include <stdio.h>

int main() {
    int N;  // 배달해야 할 설탕의 무게 (kg)
    scanf("%d", &N);

    int count = 0;  // 사용한 봉지의 개수 저장 변수
    
    // 5kg 봉지를 최대한 많이 사용하면서, 남은 무게를 3kg 봉지로 나눌 수 있는지 확인
    while (N >= 0) { 
        if (N % 5 == 0) { // N이 5의 배수라면, 5kg 봉지로만 구성 가능
            count += N / 5;  // 5kg 봉지 개수 추가
            printf("%d\n", count); // 최소 봉지 개수 출력
            return 0; // 프로그램 종료
        }
        N -= 3;  // 5로 나눠떨어지지 않으면, 3kg 봉지를 하나 사용 (즉, N에서 3kg를 뺌)
        count++; // 3kg 봉지를 하나 추가했으므로 봉지 개수 증가
    }

    // N이 정확히 0이 되지 않고, 더 이상 3kg을 뺄 수 없는 경우 (즉, 정확한 무게를 만들 수 없는 경우)
    printf("-1"); 
    
    return 0;
}

